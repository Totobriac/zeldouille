function getTile(tile) {
  var line = Math.floor(tile / 18);
  var column = tile - (line * 18);
  return [line * 16 + line + 1, column * 16 + column + 1];
}

function drawTiles(ctx) {
  for (let i = 0; i < map1.length; i++) {
    var selectedTile = getTile(map1[i]);
    var line = Math.floor(i / 28);
    var column = i - (line * 28);
    ctx.drawImage(tiles, selectedTile[1], selectedTile[0], 16, 16, column * 32, line * 32, 32, 32);
  }
}

