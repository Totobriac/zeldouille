var caves = [swordCave];

var mapTiles = 336;


function drawCave( cave, ctx) {

  ctx.fillStyle = "black";
  ctx.fillRect(8, 8, 896, 384);

  map.obstacles = getObstaclesList(caves[cave]);
  for (let i = 0; i < mapTiles; i++) {
    var selectedTile = getTile(caves[cave][i]);
    var line = Math.floor(i / 28);
    var column = i - (line * 28);
    ctx.drawImage(tiles, selectedTile[1], selectedTile[0], 16, 16,
      Math.floor(column * 32 + 8), Math.floor(line * 32 + 8), 32, 32);
}
}
